main:
    # carrega vetor
    la s0, II
    call robot

pausa:
    li t3, 700
    loop:
        add zero, zero, zero
        addi t3, t3, -1
        bne t3, zero, loop
        ret
robot:
    # salva registradores
    addi sp, sp, -12
    sw s1, 8(sp)   
    sw s0, 4(sp)
    sw ra, 0(sp)
    # index numeros ra
    li t0, 7
